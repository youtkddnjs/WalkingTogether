package com.swsoft.walkingtogether;

public class ChattingItem {
    public String name;
    public String message;
    public String time;
    public String profile;

    public ChattingItem() {

    }

    public ChattingItem(String name, String message, String time, String profile) {
        this.name = name;
        this.message = message;
        this.time = time;
        this.profile = profile;
    }
}
